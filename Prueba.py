# Pruebas con Git y GitHub

# Comando principales:
# git --version -> versión actual de git
# git init -> inicializar un repositorio vacio
# git add -> añadir un archivo 
# git commit -> comitear(comentar) un archivo 
# git status -> estado de los commit 
# git branch -> crear una rama
# git branch --list -> ver las ramas de un repositorio
# git branch -d <rama> -> borrar una rama
# git checkout <rama> -> trabajar en una rama
# git checkout <rama> -> crear una rama y trabajar en ella
# git log -> estado actual del repositorio
# git log --graph --all --one line -> visualizar los commit en una linea
# git checkout <commit> -> moverse entre diferentes commit

